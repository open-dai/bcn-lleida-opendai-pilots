package eu.opendai.spain.apis.lleida.datapublication;

public class Parada {
	int id;
	String name;
	String coord_x;
	String coord_y;
	
	public Parada(){}
	
	public int getId() {
		return id;
	}
 
	public String getName() {
		return name;
	}
 
	public String getCoordX() {
		return coord_x;
	}
 
	public String getCoordY() {
		return coord_y;
	}
 
	public void setId(int id) {
		this.id = id;
	}
 
	public void setName(String name) {
		this.name = name;
	}
 
	public void setCoordX(String in_x) {
		this.coord_x= in_x;
	}
 
	public void setCoordY(String in_y) {
		this.coord_y= in_y;
	}
}