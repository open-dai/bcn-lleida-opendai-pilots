package eu.opendai.apis.barcelona.equipagenda;

public class EquipAgenda {
	public String address;
	public String city;
	public String code_url;
	public String district;
	public String gmapx;
	public String gmapy;
	public String id;
	public String internetref;
	public String name;
	public String phonenumber;
	public String type;
	public String pos;
	public String tp;
	
	public EquipAgenda(){}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCode_url() {
		return code_url;
	}

	public void setCode_url(String code_url) {
		this.code_url = code_url;
	}

	public String getDistrict() {
		return district;
	}

	public void setDistrict(String district) {
		this.district = district;
	}

	public String getGmapx() {
		return gmapx;
	}

	public void setGmapx(String gmapx) {
		this.gmapx = gmapx;
	}

	public String getGmapy() {
		return gmapy;
	}

	public void setGmapy(String gmapy) {
		this.gmapy = gmapy;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getInternetref() {
		return internetref;
	}

	public void setInternetref(String internetref) {
		this.internetref = internetref;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhonenumber() {
		return phonenumber;
	}

	public void setPhonenumber(String phonenumber) {
		this.phonenumber = phonenumber;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getPos() {
		return pos;
	}

	public void setPos(String pos) {
		this.pos = pos;
	}

	public String getTp() {
		return tp;
	}

	public void setTp(String tp) {
		this.tp = tp;
	}
}
