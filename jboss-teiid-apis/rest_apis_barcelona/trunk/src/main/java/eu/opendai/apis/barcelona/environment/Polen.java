package eu.opendai.apis.barcelona.environment;

public class Polen {
	public String tipo_polen;
	public String concentracion;
	public String variacion;
	
	public Polen(){}

	public String getTipo_polen() {
		return tipo_polen;
	}

	public void setTipo_polen(String tipo_polen) {
		this.tipo_polen = tipo_polen;
	}

	public String getConcentracion() {
		return concentracion;
	}

	public void setConcentracion(String concentracion) {
		this.concentracion = concentracion;
	}

	public String getVariacion() {
		return variacion;
	}

	public void setVariacion(String variacion) {
		this.variacion = variacion;
	}	
}
