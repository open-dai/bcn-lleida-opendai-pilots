package eu.opendai.apis.barcelona.equipagenda;

public class EquipAgendaDetailCategory {
	public String codetype;
	public String code;
	public String name;

	public EquipAgendaDetailCategory(){}

	public String getCodetype() {
		return codetype;
	}

	public void setCodetype(String codetype) {
		this.codetype = codetype;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
